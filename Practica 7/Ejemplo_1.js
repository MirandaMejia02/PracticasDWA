// Definición de un objeto literal que representa una persona
const persona = {
    nombre: "Miguel",        // Propiedad: nombre de la persona
    edad: 20,                // Propiedad: edad de la persona
    profesion: "Ingeniero"   // Propiedad: profesión de la persona
};

// Acceso a las propiedades del objeto
console.log(persona.nombre);
console.log(persona.edad);
console.log(persona.profesion);
